/*------------------------------------------------------------------------------
VARIABLE NOTES: 
--------------------------------------------------------------------------------

VAR VARIABLE:

    - Used from 1995 to 2015 (Up to ES6) .
    - Useful for older browsers.
    - Recommended to use 'let' instead of 'var' since ES6.

LET VARIABLE:

    - camelCase (recommended).
    - Must be declared before using.
    - Can NOT be redeclared using the 'let' keyword.
    - Value can change during its lifetime.
    - Block Scope.

CONST VARIABLE:

    - SCREAMING_SNAKE_CASE (recommended).
    - Can NOT be redeclared or reassigned.
    - Block Scope.

APPLIES TO ALL VARIABLES:

    - Variables in JS are case-sensitive; "x" is different from "X".
    - A variable that is not declared will be "undefined".
    - Variable names can NOT start with a number, or contain spaces or hyphens.
    - Variable names can NOT be a reserved JavaScript keywords.

------------------------------------------------------------------------------*/